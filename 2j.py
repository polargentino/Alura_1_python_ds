'''
8 - Podemos recopilar el numerador y denominador con 
input y convertir la salida a números enteros con la 
función int(). Luego, podemos mostrar el resultado del 
resto de la división entre los dos valores con %.

'''
numerador = int(input('Ingrese el numerador: '))
denominador = int(input('Ingrese el denominador (OBS: el valor no puede ser nulo): '))
print(numerador % denominador)
