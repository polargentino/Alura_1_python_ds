'''
6 - Podemos recopilar el operador y potencia con input y 
convertir la salida a números enteros con la función 
int(). Luego, podemos mostrar el resultado de la 
exponenciación entre estos dos valores con **.
'''
operador = int(input('Ingrese el valor del operador: '))
potencia = int(input('Ingrese el valor de la potencia: '))
print(operador ** potencia)